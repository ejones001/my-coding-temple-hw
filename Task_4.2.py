#Arithmetic Operations in Daily Life

# Initial amount in the savings account
initial_amount = 10000

# Fixed yearly interest rate (in percentage)
interest_rate = 7

# Convert interest rate from percentage to decimal
interest_rate_decimal = interest_rate / 100

# Calculate the amount of interest earned
interest_earned = initial_amount * interest_rate_decimal

# Calculate the total amount in the account after a year
total_amount = initial_amount + interest_earned