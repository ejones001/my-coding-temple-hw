# Understanding Assignments and Comparisons
num1 = 5
num2 = 10

# Swap values 
num1, num2 = num2, num1

# Compare to ensure they have been swapped
print("number1 after swapping:", num1)
print("number2 after swapping:", num2)

# Comparison to ensure they have been swapped
if num1 == 10 and num2 == 5:
    print("Values have been successfully swapped.")
else:
    print("Values have not been swapped.")
